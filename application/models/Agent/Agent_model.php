class Agent_model extends CI_Model {
   public $id;	
   function __construct(){
        parent::__construct();
   }	
	
	public function json(){
		$this->datatables->select('
			agent.id as id,
			agent.nama as nama,
			agent.tl as tl,
			agent.status as status,
		');
		
		$this->datatables->from('agent');

		
		
