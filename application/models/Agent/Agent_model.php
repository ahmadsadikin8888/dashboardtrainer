		return $q;
	}
	

   public function get_all(){
		$afield = array(
			'agent.id as id',
			'agent.nama as nama',
			'agent.tl as tl',
			'agent.status as status',
		
		);
		$this->db->select($afield);

		$this->db->order_by('agent.id', 'ASC');
		return $this->db->get('agent')->result_array();
   }


	public function get_by_id($id){
		$afield = array(
			'agent.id as id',
			'agent.nama as nama',
			'agent.tl as tl',
			'agent.status as status',
		
		);
		$this->db->select($afield);

		$this->db->where('agent.id', $id);
		$this->db->order_by('agent.id', 'ASC');
		return $this->db->get('agent')->row();
   }


	/* Memastikan data yg dibuat tidak kembar/sama,
	   fungsi ini sebagai pengganti fungsi primary key dr db,
	   krn primary key sudah di gunakan untuk column id.
	   -create : id di kosongkan.
	   -update : id di isi dengan id data yg di proses.	
	*/	
	function if_exist($id,$data){
		$this->db->where('agent.id <>',$id);

		$q = $this->db->get_where('agent', $data)->result_array();
		
		if(count($q)>0){
			return true;
		}else{
			return false;
		}		

	

	}


	function insert($data){
	
	    /* transaction rollback */
		$this->db->trans_start();
		
		$this->db->insert('agent', $data);		
		/* id primary yg baru saja di input*/
		$this->id = $this->db->insert_id(); 
		
		$this->db->trans_complete();
		return $this->db->trans_status(); //return true or false
	}

	function update($id,$data){

		/* transaction rollback */
		$this->db->trans_start();

		$this->db->where('agent.id', $id);
		$this->db->update('agent', $data);
		
		$this->db->trans_complete();
		return $this->db->trans_status(); //return true or false	
	}

	function delete_multiple($data){
		/* transaction rollback */
		$this->db->trans_start();
		
		if(!empty($data)){
			$this->db->where_in('agent.id',$data);	
	
			$this->db->delete('agent');
		}
		
		$this->db->trans_complete();
		return $this->db->trans_status(); //return true or false	
		
	}


};

